<template>
  <div class="login-container">
    <v-snackbar v-model="lfalse" color="error" :timeout="3000" top> {{ errormsg }} </v-snackbar>
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login </h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
           <v-icon dark >person</v-icon>
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <v-icon dark >lock</v-icon>
          </span>
          <el-input
          
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            placeholder="Password"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
             <v-icon dark>{{ passwordType === 'password' ? 'visibility_off' : 'visibility_off'}}</v-icon>
          </span>
        </el-form-item>
      </el-tooltip>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>
    </el-form>
    
  </div>

</template>

<script>
import { validUsername } from '@/utils/validate'
import {AuthenService} from '../service/authen'
import Cookies from 'js-cookie';

const authentication = new AuthenService();

export default {
  name: 'Login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('The username is same you email address'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: 'wanchai@vstecs.co.th',
        password: 'wanchai00--'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      redirect: undefined,
      otherQuery: {},
      lfalse: false,
      errormsg: '',
      accessToken: Cookies.get('accessToken')
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
     if (this.accessToken) {
        this.$router.push({ path: this.redirect || '/app', query: this.otherQuery })
      }

    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    checkCapslock({ shiftKey, key } = {}) {
      if (key && key.length === 1) {
        if (shiftKey && (key >= 'a' && key <= 'z') || !shiftKey && (key >= 'A' && key <= 'Z')) {
          this.capsTooltip = true
        } else {
          this.capsTooltip = false
        }
      }
      if (key === 'CapsLock' && this.capsTooltip === true) {
        this.capsTooltip = false
      }
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    getMe() {
      authentication.me(this.loginForm)
        .then((data) => {
          // console.log('mydata :' + JSON.stringify(data.data[0]));
          window.ME = data.data[0];
          // console.log(window.ME);
          this.loading = false
          this.$router.push({ path: this.redirect || '/app', query: this.otherQuery })
        })
        .catch(() => {
          this.getMe()
        })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          authentication.login(this.loginForm)
          .then((data) => {
            const accessToken = data.data.accessToken;
            // const xconf = data.config.data
            Cookies.set('accessToken', accessToken, { expires: 1 });
            Cookies.set('loginid', this.loginForm , { expires: 1 });
            this.getMe();
          })
          .catch((error) => {
              if (error) {
                this.lfalse = true;
                this.errormsg = 'Error: ' + error.response.data;
                this.loading = false;
              }
            })

        } else {
          // console.log('error submit!!')
          this.lfalse = true;
          this.errormsg = 'Error: Data submitted do not complete';
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

